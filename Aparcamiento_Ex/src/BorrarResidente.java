    private void jBborrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBborrarActionPerformed
        // TODO add your handling code here:
        String sql;
        int error;
        try {
            con = new ConectaBBDD();
            con.conecta();
            con.crearSentencia();
            sql = "DELETE FROM vehiculos where matricula = '"+jComboMatricula.getSelectedItem()+"'";
            error = con.updateSQL(sql);
            if(error==1) {                  
              JOptionPane.showMessageDialog(this,"Vehiculo borrado","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }  
            if (error == -1)
                JOptionPane.showMessageDialog(this,"El vehículo está en el parking","AVISO",JOptionPane.INFORMATION_MESSAGE);
         
            this.dispose();
        }
        catch (SQLException ex) {
             JOptionPane.showMessageDialog(this,ex.getMessage(),"AVISO",JOptionPane.ERROR_MESSAGE);
 
        }
        catch (Exception ex) {
             JOptionPane.showMessageDialog(this,ex.getMessage(),"AVISO",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jBborrarActionPerformed

