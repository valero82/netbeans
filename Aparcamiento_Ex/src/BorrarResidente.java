    private void mostrar() {         
        try {
            jTmarca.setText(con.rs.getString("marca"));
            jTmodelo.setText(con.rs.getString("modelo"));
            jTtipo.setText(con.rs.getString("tipo"));  
            jTcolor.setText(con.rs.getString("color"));
            if (con.rs.getInt("numPuertas")==3)
                jRadio3p.setSelected(true);
            else 
                jRadio5p.setSelected(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(MuestraVehiculos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    void bloqueaCampos() {
        jTmarca.setEditable(false);
        jTmodelo.setEditable(false);
        jTtipo.setEditable(false);
        jTcolor.setEditable(false);
        jBborrar.setEnabled(false);
        jButton2.setEnabled(false);
    }
        private void llenaCombo() {
        try {
            ConectaBBDD con = new ConectaBBDD();
            con.conecta();
            con.crearSentencia();
            String sql = "SELECT * FROM vehiculos where clase = '01'";
            con.ejecutaSQL(sql);
            DefaultComboBoxModel modeloCombo = new DefaultComboBoxModel();
    
            while (con.rs.next())
            
            modeloCombo.addElement(con.rs.getString("matricula"));  
             
            
             jComboMatricula.setModel(modeloCombo);//con esto lo agregamos al objeto al jcombobox
             con.cerrarConexion();
        } catch (SQLException ex) {
            Logger.getLogger(EntradaSalida.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
