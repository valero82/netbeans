    private void jBmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBmodificarActionPerformed
        // TODO add your handling code here:
         
            String marca = jTmarca.getText();
            String modelo = jTmodelo.getText();
            String tipo = String.valueOf(jComboTipo.getSelectedIndex());
            String color = jComboColor.getSelectedItem().toString();
            int numPuertas;            
            if (jRadio3p.isSelected()) {
                numPuertas = 3;
            } else {
                numPuertas = 5;
            }
            
        String sql;
        int error;
        
        try {
            con = new ConectaBBDD();
            con.conecta();
            con.crearSentencia();
            sql = "UPDATE vehiculos SET marca = '"+marca+"', modelo = '"+modelo+"', tipo='"+tipo+"', color='"+color+"', numPuertas='"+numPuertas+"' WHERE matricula ='"+jComboMatricula.getSelectedItem()+"'";
              
            error = con.updateSQL(sql);
            if(error==1) {                  
              JOptionPane.showMessageDialog(this,"Vehiculo modificado","AVISO",JOptionPane.INFORMATION_MESSAGE);
            }  
            if (error == -1)
                JOptionPane.showMessageDialog(this,"No modificado","AVISO",JOptionPane.INFORMATION_MESSAGE);
         
            this.dispose();
        }
        catch (SQLException ex) {
             JOptionPane.showMessageDialog(this,ex.getMessage(),"AVISO",JOptionPane.ERROR_MESSAGE);
 
        }
        catch (Exception ex) {
             JOptionPane.showMessageDialog(this,ex.getMessage(),"AVISO",JOptionPane.ERROR_MESSAGE);
            }
    }//GEN-LAST:event_jBmodificarActionPerformed

