    private void mostrar() {         
        try {
            if (con.rs.getString("tipo").equalsIgnoreCase("01")) 
                jTtipo.setText("Monovolumen");
                else if (con.rs.getString("tipo").equalsIgnoreCase("02")) 
                jTtipo.setText("Berlina");
                else if (con.rs.getString("tipo").equalsIgnoreCase("03")) 
                jTtipo.setText("Familiar");
                else if (con.rs.getString("tipo").equalsIgnoreCase("04")) 
                jTtipo.setText("Todoterreno");
            
            Residente resi = null;
            jTmatricula.setText(con.rs.getString("matricula"));
            jTmarca.setText(con.rs.getString("marca"));
            jTmodelo.setText(con.rs.getString("modelo"));
            //jTtipo.setText(con.rs.getString("tipo"));  
            jTcolor.setText(con.rs.getString("color"));
            if (con.rs.getInt("numPuertas")==3)
                jRadio3p.setSelected(true);
            else 
                jRadio5p.setSelected(true);
            jTacumulado.setText(String.valueOf(con.rs.getInt("tiempoAcumulado")*resi.getPrecioMinuto()));
            
        } catch (SQLException ex) {
            Logger.getLogger(MuestraVehiculos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

